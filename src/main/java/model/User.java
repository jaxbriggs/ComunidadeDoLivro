package model;

/**
 *
 * @author carlos
 */
public class User {
    private int id;
    private String name;
    private Endereco endereco;
    private String email;
    private String senha;
    private boolean isAdmin;
    private boolean isAtivo;
    private String cpf;
    private String cnpj;
    private String telefone;
    private String celular;
    private String login;

    public User() {
    }

    public User(int id, String name, Endereco endereco, String email, String senha, boolean isAdmin, boolean isAtivo, String cpf, String cnpj, String telefone, String celular, String login) {
        this.id = id;
        this.name = name;
        this.endereco = endereco;
        this.email = email;
        this.senha = senha;
        this.isAdmin = isAdmin;
        this.isAtivo = isAtivo;
        this.cpf = cpf;
        this.cnpj = cnpj;
        this.telefone = telefone;
        this.celular = celular;
        this.login = login;
    }
    
    public User(String name, Endereco endereco, String email, String senha, boolean isAdmin, boolean isAtivo, String cpf, String cnpj, String telefone, String celular, String login) {
        this.id = id;
        this.name = name;
        this.endereco = endereco;
        this.email = email;
        this.senha = senha;
        this.isAdmin = isAdmin;
        this.isAtivo = isAtivo;
        this.cpf = cpf;
        this.cnpj = cnpj;
        this.telefone = telefone;
        this.celular = celular;
        this.login = login;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Endereco getEndereco() {
        return endereco;
    }

    public void setEndereco(Endereco endereco) {
        this.endereco = endereco;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public boolean isIsAdmin() {
        return isAdmin;
    }

    public void setIsAdmin(boolean isAdmin) {
        this.isAdmin = isAdmin;
    }

    public boolean isIsAtivo() {
        return isAtivo;
    }

    public void setIsAtivo(boolean isAtivo) {
        this.isAtivo = isAtivo;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }
    
    
    
}
